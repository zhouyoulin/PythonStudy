# 整型
i = 30
print(i)
# type()函数返回变量的数据类型
print(type(i))

# 浮点类型
a = 12.35
print(a)
print(type(a))

# 布尔值
# 布尔值只有两种结果True和False 对应二进制中的0和1
b = 1
print(b == 1)
print(b == 3)

# 字符串
# 单引号之间的单行字符串
s = 'a'
print(s)
print(type(s))

# 双引号之间的单行字符串
s1 = "as myself"
print(s1)
print(type(s1))

# 三个引号之间的多行字符串
s2 = ''' as me
you are 
my friend
'''
print(s2)
print(type(s2))